using UnityEngine;
using System.Collections;

public class PlayerController : MonoBehaviour 
{
	public float speed;
	public GameObject Bullet;
	public GameObject newbullet;
	void Start()
	{
		//setCountText();
	}
	void FixedUpdate()
	{
		float moveHorizontal = Input.GetAxis ("Horizontal");
		float moveVertical = Input.GetAxis ("Vertical");
		Vector3 movement = new Vector3 (moveHorizontal, 0, moveVertical);
		GetComponent<Rigidbody>().AddForce (movement * speed * Time.deltaTime);

		if (Input.GetKeyDown ("space"))
		{   
		    Fire ();
			//newbullet=Instantiate (Bullet,transform.position,transform.rotation);
			//newbullet.transform.position += transform.forward * Time.deltaTime * speed;
		}
	}
	// Fire a bullet 
	void Fire () {
		// Create a new bullet pointing in the same direction as the gun 
		newbullet=Instantiate (Bullet,transform.position,transform.rotation) as GameObject;;
	} 

}
